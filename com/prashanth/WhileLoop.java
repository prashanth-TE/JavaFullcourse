package com.prashanth;

public class WhileLoop {
    public static void main(String[] args) {
        int count =1;
        while (count != 5) {
            System.out.println(count);
            count++;
        }
    }
}
// Here while will check the condition first and then execute the block of code.
// If the condition is false then it will not execute the block of code.
// If the condition is true then it will execute the block of code.

