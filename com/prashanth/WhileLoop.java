package com.prashanth;

public class WhileLoop {
    public static void main(String[] args) {
        int n = 5;
        int i = 1;
        while (i <= n) {
            System.out.println(i);
            i++;
        }
    }
}
//   Here in the above line number 7, we are checking the condition i<=n
//   If the condition is true, then the code inside the while loop will be executed else it will come out of the loop.
// If the condition is true then the code inside the while loop will be executed and the value of i will be incremented by 1. and again the condition will be checked.
// This process will be repeated until the condition becomes false. Once the condition becomes false, the control will come out of the loop.
